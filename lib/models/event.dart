class Event {
  final String id;
  final String title;
  final String description;
  final String date;
  final String time;
  final String speaker;
  final String location;
  final String poster;

  const Event({
    required this.id,
    required this.title,
    required this.description,
    required this.date,
    required this.time,
    required this.speaker,
    required this.location,
    required this.poster,
  });
}
